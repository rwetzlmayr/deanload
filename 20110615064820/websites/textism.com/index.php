<p style="text-align:center">* &nbsp; * &nbsp; *</p><h3><a href="/article/780/ReferSpamming" title="Permanent link to this article">Refer Spamming</a> &#183; 6 days ago</h3>
	<p>A lot of people using <a href="/tools/refer/">Refer</a> have noticed the increasingly frequent appearance of spurious referrers such britney-spears-breakfast-beers.blogspot.com in their referrer lists.</p>

	<p>I&#8217;ve been seeing this junk recorded by other traffic reporting software, thus I don&#8217;t think Refer installs are being specifically targeted; nonetheless measures should be taken to silence this incredibly dim-witted &#8216;marketing&#8217;. </p>

	<p>In the current <a href="/refer/">version</a> of Refer running on this site I&#8217;ve added a filtering mechanism which disallows multiple referrers to be delivered by the same visitor address, like, oh I don&#8217;t know, hmdnsgroup.com or h24-69-156-45.no.shawcable.net. </p>

	<p>The update is still too flaky to release, and <a href="http://textpattern.com">this</a> is taking up all available time, however those running older versions of Refer can take advantage of <a href="http://astrogibs.com/eric/archives/technology/botwhack_10.html">this patch</a> by Eric Goldberg, which filters spammers based on their user-agent headers.</p>


 
