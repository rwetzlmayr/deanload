<title>Something Soup</title>
<description>	&lt;p&gt;Put a soup pot over a low fire, and chuck in a really rather unreasonably large quantity of unsalted butter. While it melts, peel and dice (thup thup thup) a couple white or yellow (not spanish, not vidalia) onions. Make sure any stray onionskin has been removed; it will bring disappointment later. Also don&amp;#8217;t breathe through your nose while dicing unless you enjoy tears and pain. Into the pot.&lt;/p&gt;

	&lt;p&gt;Key thing here is to sweat out some of the sugars from, but under no circumstances brown, the onions. Keep the flame low and stir frequently with a wooden spoon, ideally the kind that&amp;#8217;s sort of squared off at the end so you can scrape up anything that gets stuck. If, due to your own inattentiveness, hard dry bits of onion appear and the whole lot begins to caramelize, abandon the project and go away until you&amp;#8217;re ready to take this seriously.&lt;/p&gt;

	&lt;p&gt;Pare, rinse, and roughly chop a reasonable quantity (say, as much as you can scoop up with both hands in two gos) of something.&lt;sup&gt;1&lt;/sup&gt; Once the onions are nearly translucent, turn up the heat, add in the something, and stir and turn and toss and shake the pot until everything sizzles and any water that came with the something has evaporated. Should be a couple minutes.&lt;/p&gt;

	&lt;p&gt;As you know, it&amp;#8217;s easy enough to have chicken stock on hand. Simmer some chicken bones (if you have no bones, buy a couple of those family packs of chicken wings) in water for a few hours&lt;sup&gt;2&lt;/sup&gt; with carrots, onions, celery, parsley stems, a bit of thyme and a few peppercorns. It&amp;#8217;s impossible to screw up so long as you never let the stock come to a rolling boil, which will cause it to  become cloudy and gross.&lt;sup&gt;3&lt;/sup&gt;&lt;/p&gt;

	&lt;p&gt;Add hot stock to the soup pot&lt;sup&gt;4&lt;/sup&gt; until it seems like there&amp;#8217;s enough. Bring it almost to a boil, then turn the heat down and simmer for precisely twenty one and a half minutes, then remove the pot from the stove. Dig out the Braun handheld doohickey and puree the soup until completely smooth, or transfer it in batches to the food processor or blender to achieve the same result.&lt;/p&gt;

	&lt;p&gt;Check for salt, then stir in a really rather unreasonably large quantity of heavy cream. Serve in heated soup plates with croutons or a sprig of something nice.&lt;/p&gt;

	&lt;ol&gt;
		&lt;li&gt;This could be broccoli, green beans, fresh peas, celery, artichoke hearts, cauliflower, carrots or asparagus tips; just about any vegetable, really.&lt;/li&gt;
			&lt;li&gt;When the &lt;a href="http://megnut.com" title=""&gt;Hourihan&lt;/a&gt; /  &lt;a href="http://kottke.org" title=""&gt;Kottke&lt;/a&gt; alliance were here for a visit in November, this was Jason looking at a mess of sorry vegetables and used-up bones at the bottom of the stock pot: &lt;em&gt;You just going to throw that away?&lt;/em&gt;&lt;/li&gt;
			&lt;li&gt;As long as the stock is brought to a simmer once a day it can go on almost perpetually: skim occasionally and ladle off what you need, and add chicken and aromatic vegetables whenever it seems appropriate. Keep the lid off overnight: the stock will only sour if left at room temperature with a lid on.&lt;/li&gt;
			&lt;li&gt;There&amp;#8217;s something resoundingly satisfying about ladling hot stock from one pot to another. I have no idea why.&lt;/li&gt;
	&lt;/ol&gt;
